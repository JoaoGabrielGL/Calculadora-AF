package CalculadoraJunitTest;

/**
 * @version 1.1
 * @author João Gabriel
 * @since Release 1.1 - 16/11/2023
 *
 * classe representante a operação de multiplicação da calculadora.
 */
public class multiplacao {

    public multiplacao() {
    }

    /**
     * Método que ira fazer a operação de multiplicação.
     *
     * @param a Double - O valor do primeiro número.
     * @param b Double - O valor do segundo número.
     * @return Double - O resultado da multiplicação.
     */
    public double Multplicacao(double a, double b) {
        return a * b;
    }
}
